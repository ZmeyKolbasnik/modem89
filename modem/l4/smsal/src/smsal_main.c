    smsal_write_aux_info_struct *aux_info;

    aux_info = (smsal_write_aux_info_struct *) (&(smsal_cntx_ptr->pending_act[old_id]))->aux_info;
    data_ptr = (kal_uint8 *) (&(smsal_cntx_ptr->pending_act[old_id]))->peer_buff_ptr;

    scts_ptr = NULL;

    entry_id = aux_info->index;
    entry = (&(smsal_cntx_ptr->message_box[entry_id]));

    /* Mark the state of MB entry to original values */
    (&(smsal_cntx_ptr->message_box[entry_id]))->state &= (~SMSAL_MB_ENTRY_PENDING);
    /*
     * Update message box: state, status
     */
    entry->state = SMSAL_MB_ENTRY_VALID;

    if (data_ptr[0] == SMSAL_UNREAD_MSG)
        entry->status = SMSAL_STATUS_UNREAD;
    else
        entry->status = SMSAL_STATUS_UNSENT;

    INCR_MSGBOX_NUM(entry->status, entry->storage_type);

    /* write next one */
    ret = smsal_get_free_mb_entry((smsal_storage_enum) aux_info->storage_type, &aux_info->index);
    if (ret == KAL_TRUE)
    {
        (&(smsal_cntx_ptr->pending_act[old_id]))->local_para_ptr = NULL;
        (&(smsal_cntx_ptr->pending_act[old_id]))->peer_buff_ptr = (peer_buff_struct *) data_ptr;
        smsal_fill_sms_pdu(data_ptr, 0xff, aux_info->index);
        smsal_tst_write_req(old_id, data_ptr, scts_ptr);
    }
    else
    {
        free_ctrl_buffer(data_ptr);
        (&(smsal_cntx_ptr->pending_act[old_id]))->peer_buff_ptr = NULL;
        smsal_free_pending_act(old_id);
    }

