void smsal_itoa(kal_uint16 n, kal_uint8 *s)
{
    kal_uint8 i = 0, j, k, c;

    do
    {
        s[i++] = n % 10 + '0';
    } while ((n /= 10) > 0);
    s[i] = '\0';

    /* reverse */
    for (j = 0, k = i - 1; j < k; j++, k--)
    {
        c = s[j];
        s[j] = s[k];
        s[k] = c;
    }
    return;
}

void smsal_fill_sms_pdu(kal_uint8 *data, kal_uint8 msg_status, kal_uint16 entry_id)
{

    kal_uint8 temp_data[8];
    kal_uint8 data_len;
    kal_uint8 data_index;

    kal_mem_set((void *)(data + 1), 0xff, 175);

    if (msg_status != 0xff)
        data[0] = msg_status;           /* status */

    data[1] = 0x07;                     /* sca */
    data[2] = 0x91;
    data[3] = 0x11;
    data[4] = 0x11;
    data[5] = 0x11;
    data[6] = 0x11;
    data[7] = 0x11;
    data[8] = 0x11;

    if ((data[0] == SMSAL_UNREAD_MSG) ||
        (data[0] == SMSAL_READ_MSG))
    {
        /* MT SMS DELIVER */
        data[9] = 0x04;                 /* fo */
        data[10] = 0x0c;                /* oa */
        data[11] = 0x91;
        data[12] = 0x22;
        data[13] = 0x22;
        data[14] = 0x22;
        data[15] = 0x22;
        data[16] = 0x22;
        data[17] = 0x22;
        data[18] = 0x00;                /* pid */
        data[19] = 0x00;                /* dcs */
        data[20] = 0x80;                /* scts-year */
        data[21] = 0x01;                /* scts-mon */
        data[22] = 0x02;//(entry_id % 30) & 0xf5;      /* scts-day */
        data[23] = 0x11;//0x51;                /* scts-hr */
        data[24] = 0x11;//(rand() % 60) & 0xf5;        /* scts-min */
        data[25] = 0x11;//(entry_id % 60) & 0xf5;      /* scts-sec */
        data[26] = 0x2a;                /* scts-timezone */
        data_index = 27;
    }
    else
    {
        data[9] = 0x11;                 /* fo */
        data[10] = 0xff;                /* mr */
        data[11] = 0x00;                /* DA */
        data[12] = 0x81;
        data[13] = 0x00;                /* pid */
        data[14] = 0x00;                /* dcs */
        data[15] = 0x47;                /* VP */
        data_index = 16;
    }

    /* data[28] user data, content */
    smsal_itoa(entry_id, temp_data);
    data_len = (kal_uint8)strlen((kal_char *) temp_data);

    data[data_index] = (data_len * 7 + 7) / 8;

    smsal_gsm7_pack(temp_data,          /* unpacked */
                    &data[data_index + 1],      /* packed */
                    data_len,           /* no. of text in chars */
                    0);

}

void smsal_tst_write_req(kal_uint8 id, kal_uint8 *data, kal_uint8 *scts)
{
    smsal_context_struct *smsal_cntx_ptr=smsal_ptr_g;
    smsal_write_aux_info_struct *aux_info;
    smsal_pdu_decode_struct sms_pdu;
    smsal_tpdu_decode_struct *tpdu_ptr;
    kal_bool ret_val;

    aux_info = (smsal_write_aux_info_struct *) (&(smsal_cntx_ptr->pending_act[id]))->aux_info;
    tpdu_ptr = &sms_pdu.tpdu;

    ret_val = smsal_decode_sms_pdu(&data[1], SMSAL_SMS_LEN - 1, &sms_pdu);
    if (ret_val == KAL_FALSE)
        smsal_free_tpdu_decode_struct(tpdu_ptr);

    if (ret_val == KAL_TRUE)
    {
        smsal_write_short_msg(id,
                              (smsal_storage_enum) aux_info->storage_type,
                              (&(smsal_cntx_ptr->message_box[aux_info->index]))->record_no,
                              data,
                              scts,
                              SMSAL_SMS_LEN);
    }

    if (ret_val == KAL_FALSE)
    {
        free_ctrl_buffer((&(smsal_cntx_ptr->pending_act[id]))->peer_buff_ptr);
        (&(smsal_cntx_ptr->pending_act[id]))->peer_buff_ptr = NULL;
        smsal_free_pending_act(id);
        return;
    }

    smsal_update_msgbox_from_tpdu((&(smsal_cntx_ptr->message_box[aux_info->index])), &(sms_pdu.tpdu));

    /* free the unpacked structure for TPDU */
    smsal_free_tpdu_decode_struct(tpdu_ptr);

    /* Mark the state of the message box's entry to PENDING */
    (&(smsal_cntx_ptr->message_box[aux_info->index]))->state |= SMSAL_MB_ENTRY_PENDING;
    
    INCR_USED_MSG_NUM(aux_info->storage_type);
}

